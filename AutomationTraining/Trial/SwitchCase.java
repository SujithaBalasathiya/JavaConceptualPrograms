package Trial;

public class SwitchCase {

	public static void main(String[] args) {
	//	Switch and if-else(Conditional statements)
	//	For and while (looping statement)
		int fruit=2;
		
		switch(fruit) {
		case 1: 
			System.out.println("Apple");
			break;
		case 2:
			System.out.println("Orange");
			break;
		case 3:
			System.out.println("Banana");
			break;
		case 4:
			System.out.println("fig");
		default:
			System.out.println("Like all fruit");
		

	}

}
}
